public class p3_Relational_Operators {

    // This Code is Written By Priyanshu Gour
    
    public static void main(String args[]){

        // Relational Operators  :- it shows the output in the form of boolen .

        int a = 10 ;
        int b = 20 ;

        System.out.println(a==b); // == check value is equal or not..
        System.out.println(a!=b); // != check value is not equal ..
        System.out.println(a>b); // > a is greater or not ..
        System.out.println(a<b); // < b is greater or not ..
        System.out.println(a>=b); // a is greater equal or not ..
        System.out.println(a<=b); // b is greater equal or not ..
    }
}
