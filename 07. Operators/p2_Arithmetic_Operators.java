public class p2_Arithmetic_Operators {
    public static void main(String args[]){


        // ARithmetic Operators...

        int a = 10 ;
        int b = 5 ; 

        // Binary Arithmetic Operators
        int sum = a + b ; // sum Arithmetic Operators.
        int sub = a - b ; // sub Arithmetic Operators.
        int mul = a * b ; // mul Arithmetic Operators.
        int div = a / b ; // divided Arithmetic Operators.
        int mod = a % b ; // modulo This is showes the Remiender.

        System.out.println(sum);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(mod);

        //Unary Arithmetic Operators..

        int incpost = a++ ; // post increment 
        int incpre = ++a ; // pre icrement 

        int decpost = a-- ; // post decrement 
        int decpre = --a ; // pre decrement 

        System.out.println(incpost);
        System.out.println(incpre);

        System.out.println(decpost);
        System.out.println(decpre);
        
    }
}
