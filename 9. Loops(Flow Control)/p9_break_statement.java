public class p9_break_statement {
    public static void main(String args[]){

        // this code is writen by Priyanshu Gour


        // break :- to exit the loop ..

        for(int i = 1 ; 10 >= i ; i++){
            if(i == 5){
                break; // to exit the loop.
            }
            System.out.println(i);
        }


    }
}
