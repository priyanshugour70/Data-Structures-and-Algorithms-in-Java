public class p5_ASsignment_Operators {
    public static void main(String args[]){


        // Assignment Operators..

        int a = 10 ;
        int b = 5 ;
    
        b = a ;
        System.out.println(b);
        
        b += a ;
        System.out.println(b);
        
        b -= a ;
        System.out.println(b);
        
        b *= a ;
        System.out.println(b);
        
        b /= a ;
        System.out.println(b);
        

    }
}
